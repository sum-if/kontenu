        private void FrmTransferKas_Load(object sender, EventArgs e) {
            MySqlConnection con = new MySqlConnection(OswConfig.KONEKSI);
            MySqlCommand command = con.CreateCommand();
            MySqlTransaction trans;

            try {
                // buka koneksi
                con.Open();

                // set transaction
                trans = con.BeginTransaction();
                command.Transaction = trans;

                // Function Code
                DataOswJenisDokumen dOswJenisDokumen = new DataOswJenisDokumen(command, id);
                this.dokumen = dOswJenisDokumen.nama;
                this.Text = this.dokumen;

                OswControlDefaultProperties.setBrowse(this, id, command);
                OswControlDefaultProperties.setTanggal(deTanggalAwal);
                OswControlDefaultProperties.setTanggal(deTanggalAkhir);
                deTanggalAwal.DateTime = OswDate.getDateTimeTanggalAwalBulan();
                deTanggalAkhir.DateTime = OswDate.getDateTimeTanggalHariIni();

                cmbAsal = ComboQueryUmum.getAkun(cmbAsal, command, "akun_kas_bank", true);
                cmbAsal.EditValue = OswCombo.getFirstEditValue(cmbAsal);

                cmbTujuan = ComboQueryUmum.getAkun(cmbTujuan, command, "akun_kas_bank", true);
                cmbTujuan.EditValue = OswCombo.getFirstEditValue(cmbTujuan);

                this.setGrid(command);

                // Commit Transaction
                command.Transaction.Commit();
            } catch(MySqlException ex) {
                OswPesan.pesanErrorCatch(ex, command, dokumen);
            } catch(Exception ex) {
                OswPesan.pesanErrorCatch(ex, command, dokumen);
            } finally {
                con.Close();
                try {
                    SplashScreenManager.CloseForm();
                } catch(Exception ex) {
                }
            }
        }

        public void setGrid(MySqlCommand command) {
            String strngTanggalAwal = deTanggalAwal.Text;
            String strngTanggalAkhir = deTanggalAkhir.Text;
            String strngKode = txtKode.Text;
            String strngAsal = cmbAsal.EditValue.ToString();
            String strngTujuan = cmbTujuan.EditValue.ToString();

            String query = @"SELECT A.kode AS Nomor, A.tanggal AS Tanggal, B.kode AS 'Akun Asal', B.nama AS 'Nama Akun Asal', C.kode AS 'Akun Tujuan', C.nama AS 'Nama Akun Tujuan', A.nominal AS Nominal,A.catatan AS Catatan
                            FROM transferkas A
                            INNER JOIN akun B ON A.akunawal = B.kode
                            INNER JOIN akun C ON A.akunakhir = C.kode
                            WHERE toDate(A.tanggal) BETWEEN toDate(@tanggalawal) AND toDate(@tanggalakhir) AND                                   
                                  A.kode LIKE @kode AND A.akunawal LIKE @akunawal AND A.akunakhir LIKE @akunakhir
                            ORDER BY A.kode";

            Dictionary<String, String> parameters = new Dictionary<String, String>();
            parameters.Add("tanggalawal", strngTanggalAwal);
            parameters.Add("tanggalakhir", strngTanggalAkhir);
            parameters.Add("kode", "%" + strngKode + "%");
            parameters.Add("akunawal", strngAsal);
            parameters.Add("akunakhir", strngTujuan);
            

            gridControl = OswGrid.getGridBrowse(gridControl, command, query, parameters, 
                                                new String[] { },
                                                new String[] { "Nominal" });
        }

