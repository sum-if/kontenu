    public partial class FrmLaporanPostDatedCheque : DevExpress.XtraEditors.XtraForm {
        private String id = "LAPORANPOSTDATEDCHEQUE";
        private String dokumen = "LAPORANPOSTDATEDCHEQUE";

        public FrmLaporanPostDatedCheque() {
            InitializeComponent();
        }

        private void FrmLaporanPostDatedCheque_Load(object sender, EventArgs e) {
            MySqlConnection con = new MySqlConnection(OswConfig.KONEKSI);
            MySqlCommand command = con.CreateCommand();
            MySqlTransaction trans;

            try {
                // buka koneksi
                con.Open();

                // set transaction
                trans = con.BeginTransaction();
                command.Transaction = trans;

                // Function Code
                DataOswJenisDokumen dOswJenisDokumen = new DataOswJenisDokumen(command, id);
                this.dokumen = dOswJenisDokumen.nama;
                this.Text = this.dokumen;

                OswControlDefaultProperties.setInput(this, id, command);

                OswControlDefaultProperties.setTanggal(deTanggal);
                deTanggal.DateTime = OswDate.getDateTimeTanggalAwalBulan();

                // Commit Transaction
                command.Transaction.Commit();
            } catch(MySqlException ex) {
                OswPesan.pesanErrorCatch(ex, command, dokumen);
            } catch(Exception ex) {
                OswPesan.pesanErrorCatch(ex, command, dokumen);
            } finally {
                con.Close();
                try {
                    SplashScreenManager.CloseForm();
                } catch(Exception ex) {
                }
            }
        }

        private void btnCetak_Click(object sender, EventArgs e) {
            SplashScreenManager.ShowForm(typeof(SplashUtama));
            MySqlConnection con = new MySqlConnection(OswConfig.KONEKSI);
            MySqlCommand command = con.CreateCommand();
            MySqlTransaction trans;

            try {
                // buka koneksi
                con.Open();

                // set transaction
                trans = con.BeginTransaction();
                command.Transaction = trans;

                // Function Code
                RptPostDatedCheque report = new RptPostDatedCheque();
                String strngTanggal = "31/12/9999";
                report.Parameters["judulReport"].Value = "Laporan Post Dated Cheque";

                if(rdoTanggal.Checked == true) {
                    strngTanggal = deTanggal.Text;
                    report.Parameters["judulReport"].Value = "Laporan Post Dated Cheque per Tanggal " + strngTanggal;
                }

                report.Parameters["GUID"].Value = OswConstants.GUID;
                report.Parameters["tanggal"].Value = strngTanggal;


                //// Assign the printing system to the document viewer.
                LaporanPrintPreview laporan = new LaporanPrintPreview();
                laporan.documentViewer1.DocumentSource = report;

                OswLog.setLaporan(command, dokumen);

                laporan.Show();

                // Commit Transaction
                command.Transaction.Commit();
            } catch(MySqlException ex) {
                OswPesan.pesanErrorCatch(ex, command, dokumen);
            } catch(Exception ex) {
                OswPesan.pesanErrorCatch(ex, command, dokumen);
            } finally {
                con.Close();
                try {
                    SplashScreenManager.CloseForm();
                } catch(Exception ex) {
                }
            }
        }

        private void rdoAktual_CheckedChanged(object sender, EventArgs e) {
            if(rdoAktual.Checked == true) {
                deTanggal.Enabled = false;
            }
        }

        private void rdoTanggal_CheckedChanged(object sender, EventArgs e) {
            if(rdoTanggal.Checked == true) {
                deTanggal.Enabled = true;
            }
        }
    }
